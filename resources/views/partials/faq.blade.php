<script>
document.addEventListener("DOMContentLoaded", () => {
  const faqItems = document.querySelectorAll(".faq-text");

  faqItems.forEach(item => {
    const btn = item.querySelector(".faq-btn");
    const answer = item.querySelector(".faq-answer");
    const arrow = item.querySelector(".arrow");

    btn.addEventListener("click", () => {
      // Periksa apakah FAQ yang diklik sudah terbuka
      const isCurrentlyOpen = answer.classList.contains("open");

      // Tutup semua FAQ
      faqItems.forEach(i => {
        i.querySelector(".faq-answer").classList.remove("open");
        i.querySelector(".arrow").classList.remove("rotate");
      });

      // Jika FAQ yang diklik awalnya tertutup, sekarang buka
      if (!isCurrentlyOpen) {
        answer.classList.add("open");
        arrow.classList.add("rotate");
      }
    });
  });
});
</script>

